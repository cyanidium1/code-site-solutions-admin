/* ─── Portable-text builders ─────────────────────────────────────────────── */

type Span = {_type: 'span'; _key: string; text: string; marks: string[]}
type MarkDef = {_type: 'link'; _key: string; href: string; newTab?: boolean}
type Block = {
  _type: 'block'
  _key: string
  style: 'normal' | 'h2' | 'h3' | 'blockquote'
  listItem?: 'bullet' | 'number'
  level?: number
  children: Span[]
  markDefs: MarkDef[]
}
type TldrBoxBlock = {
  _type: 'tldrBox'
  _key: string
  title: string
  items: string[]
}
type CtaCalloutBlock = {
  _type: 'ctaCallout'
  _key: string
  eyebrow?: string
  heading: string
  sub?: string
  ctaLabel: string
  ctaHref: string
  ctaSecondaryLabel?: string
  ctaSecondaryHref?: string
}
type BlogTableBlock = {
  _type: 'blogTable'
  _key: string
  headers: string[]
  rows: Array<{_key: string; _type: 'blogTableRow'; cells: string[]}>
}
type BodyBlock = Block | TldrBoxBlock | CtaCalloutBlock | BlogTableBlock

/** Mini-token DSL for inline content inside paragraphs / list items. */
type InlineSeg = string | {strong: string} | {em: string} | {link: string; text: string}

class KeyGen {
  private prefix: string
  private n = 0
  constructor(prefix: string) {
    this.prefix = prefix
  }
  next(tag: string): string {
    this.n += 1
    return `${this.prefix}-${tag}-${this.n}`
  }
}

function buildSpans(
  kg: KeyGen,
  segs: InlineSeg[],
): {children: Span[]; markDefs: MarkDef[]} {
  const children: Span[] = []
  const markDefs: MarkDef[] = []
  for (const seg of segs) {
    if (typeof seg === 'string') {
      if (seg.length === 0) continue
      children.push({_type: 'span', _key: kg.next('s'), text: seg, marks: []})
    } else if ('strong' in seg) {
      children.push({
        _type: 'span',
        _key: kg.next('s'),
        text: seg.strong,
        marks: ['strong'],
      })
    } else if ('em' in seg) {
      children.push({
        _type: 'span',
        _key: kg.next('s'),
        text: seg.em,
        marks: ['em'],
      })
    } else {
      const linkKey = kg.next('m')
      markDefs.push({
        _type: 'link',
        _key: linkKey,
        href: seg.link,
        newTab: seg.link.startsWith('http'),
      })
      children.push({
        _type: 'span',
        _key: kg.next('s'),
        text: seg.text,
        marks: [linkKey],
      })
    }
  }
  return {children, markDefs}
}

function makeBlock(
  kg: KeyGen,
  style: Block['style'],
  segs: InlineSeg[],
  listItem?: Block['listItem'],
): Block {
  const {children, markDefs} = buildSpans(kg, segs)
  return {
    _type: 'block',
    _key: kg.next('b'),
    style,
    ...(listItem ? {listItem, level: 1} : {}),
    children:
      children.length > 0
        ? children
        : [{_type: 'span', _key: kg.next('s'), text: '', marks: []}],
    markDefs,
  }
}

// Convenience helpers
const p = (kg: KeyGen, ...segs: InlineSeg[]) => makeBlock(kg, 'normal', segs)
const h2 = (kg: KeyGen, text: string) => makeBlock(kg, 'h2', [text])
const h3 = (kg: KeyGen, text: string) => makeBlock(kg, 'h3', [text])
const blockquote = (kg: KeyGen, ...segs: InlineSeg[]) =>
  makeBlock(kg, 'blockquote', segs)
const bullet = (kg: KeyGen, ...segs: InlineSeg[]) =>
  makeBlock(kg, 'normal', segs, 'bullet')
const numbered = (kg: KeyGen, ...segs: InlineSeg[]) =>
  makeBlock(kg, 'normal', segs, 'number')

const strong = (text: string): InlineSeg => ({strong: text})
const link = (text: string, href: string): InlineSeg => ({link: href, text})

function tldr(kg: KeyGen, title: string, items: string[]): TldrBoxBlock {
  return {_type: 'tldrBox', _key: kg.next('tldr'), title, items}
}

function cta(
  kg: KeyGen,
  fields: Omit<CtaCalloutBlock, '_type' | '_key'>,
): CtaCalloutBlock {
  return {_type: 'ctaCallout', _key: kg.next('cta'), ...fields}
}

function table(kg: KeyGen, headers: string[], rows: string[][]): BlogTableBlock {
  return {
    _type: 'blogTable',
    _key: kg.next('tbl'),
    headers,
    rows: rows.map((cells) => ({
      _key: kg.next('row'),
      _type: 'blogTableRow' as const,
      cells,
    })),
  }
}

